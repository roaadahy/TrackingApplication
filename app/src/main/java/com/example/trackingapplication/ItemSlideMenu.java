package com.example.trackingapplication;

public class ItemSlideMenu {

    private int img_id;
    private String title;

    public ItemSlideMenu(int imd_id, String title) {
        this.img_id = imd_id;
        this.title = title;
    }

    public int getImg_id() {
        return img_id;
    }

    public String getTitle() {
        return title;
    }

}
